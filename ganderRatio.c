/*
 파일: ganderRatio.c
 역할: 사용자로부터 남자 수와 여자 수를 입력받아 총합과 각 성별의 비율을 계산하여 출력하는 예제 프로그램

 배경지식(초심자용 상세 설명):
 - C 프로그램은 `main` 함수에서 시작합니다. 프로그램은 위에서 아래로 한 줄씩 실행됩니다.
 - `#include <stdio.h>`는 표준 입출력 라이브러리를 불러옵니다. 이 라이브러리에는 `printf`와 `scanf`가 정의되어 있습니다.
 - 변수는 값을 저장하는 '그릇'입니다. `int`는 정수용(예: 1, 42), `double`은 소수점이 있는 숫자(예: 3.14)를 저장합니다.
 - `scanf`는 사용자 입력을 읽어 변수에 저장합니다. `scanf`에 넘기는 두 번째 인자는 입력을 저장할 변수의 주소(위치)를 나타내는 포인터여야 합니다. 
 - 그래서 `&man`처럼 `&` 연산자를 사용합니다.
 - 정수와 실수 간의 연산에서는 타입 변환이 자동으로 일어날 수 있습니다(예: `int` 값을 `double`로 바꿔 계산). 이 때문에 나눗셈을 할 때 
 - 정수 나눗셈/실수 나눗셈의 차이를 이해해야 합니다.
 - `printf`의 포맷 문자열(`%d`, `%.2f`, `%%` 등)은 출력할 값의 타입과 형태를 지정합니다. `%%`는 실제 퍼센트 기호(%)를 출력합니다.
 - 연산자 우선순위: 곱셈/나눗셈(`*`, `/`)은 덧셈/뺄셈(`+`, `-`)보다 먼저 계산됩니다. 괄호 `()`를 사용하면 계산 순서를 명확히 할 수 있습니다.
 더블과 인트의 차이는 무엇인가요?
 - `int`는 정수형으로 소수점이 없는 숫자를 저장합니다. 예: 1, -5, 42
 - `double`은 실수형으로 소수점이 있는 숫자를 저장합니다. 예: 3.14, -0.001, 2.0
 - `double`은 `int`보다 더 넓은 범위와 더 많은 소수 자리를 표현할 수 있습니다. 
 - `int`를 `double`로 변환하면 소수점이 붙은 형태로 바뀌지만, `double`을 `int`로 변환하면 소수점 이하가 버려집니다. 예: (double)5 -> 5.0, (int)3.14 -> 3
 - 이 프로그램에서는 `sum`과 비율 계산에서 소수점이 필요한므로 `double`을 사용합니다. 만약 `sum`이 `int`였다면, `woman / sum`이 정수 나눗셈이 되어 결과
 - 0이 될 수 있습니다.
 아래 코드는 초심자가 각 줄의 역할과 다른 줄에 미치는 영향을 이해하도록 매우 자세한 주석을 포함하고 있습니다.
*/

#include <stdio.h> // 표준 입출력 함수를 사용하기 위한 헤더: printf, scanf 선언이 여기에 있음

int main(void) // 프로그램의 시작점(main 함수). 반환형 int는 운영체제에게 종료 상태(0은 정상)를 전달함
{
    /* 변수 선언부 */
    int man, woman;                 // man: 사용자가 입력할 남자 수를 저장하는 변수(정수)
                                    // woman: 사용자가 입력할 여자 수를 저장하는 변수(정수)
                                    // 두 변수는 이후 합계(sum) 계산과 비율 계산에 사용됨

    double sum, manRate, womanRate; // sum: 남자+여자 총합을 저장(실수형으로 선언한 이유는 나눗셈 결과를 소수로 처리하기 위해)
                                    // manRate, womanRate: 각 성별 비율(퍼센트)을 실수로 저장
                                    // 만약 sum을 int로 선언했다면 소수점이 버려질 수 있으므로 double로 선언함

    /* 사용자 입력 안내 및 입력 처리 */
    printf("남자의 수는 : "); // 화면에 안내 문구를 출력. 이 줄은 단순 출력만 하고 상태를 바꾸지 않음
    // printf는 버퍼에 문자열을 보내고, 표준 출력(stdout)에 출력합니다. 사용자에게 입력을 요청하는 효과를 가짐

    scanf("%d", &man); // 사용자로부터 정수를 입력받아 man 변수에 저장
                         // 첫 번째 인자 "%d"는 정수형 포맷을 의미함
                         // 두 번째 인자는 변수의 주소(&man). 주소를 전달해야 scanf가 그 위치에 값을 저장할 수 있음
                         // 만약 &를 빼면 scanf는 주소값 대신 정수값을 포인터로 해석하려 시도해 프로그램이 비정상 종료(세그멘테이션 폴트)됨

    printf("여자의 수는 : "); // 여자 수 입력을 위한 안내문 출력
    scanf("%d", &woman);       // 사용자 입력을 읽어 woman 변수에 저장(위와 동일한 원리)

    /* 합계와 비율 계산 */
    sum = man + woman; // man과 woman의 값을 더해 sum에 저장
                       // man, woman은 int이지만 sum은 double이므로 더한 결과가 double로 암묵 변환되어 저장됨
                       // 다른 줄에 미치는 영향: sum 값이 비어있으면 다음 비율 계산에서 0으로 나누는 오류가 발생할 수 있음

    womanRate = woman / sum * 100; // 여자 비율 계산: (woman / sum) * 100
                                   // 연산 과정:
                                   // 1) woman은 int, sum은 double -> woman이 double로 암묵 변환되어 실수 나눗셈이 수행됨
                                   // 2) 나눗셈 연산 결과가 (예: 0.43) 곱하기 100을 하면 퍼센트값(예: 43.0)이 됨
                                   // 주의: sum이 0이면(모든 학생 수가 0이면) 0으로 나누기 오류가 발생하므로 입력값 검증이 필요함

    manRate = man / sum * 100;     // 남자 비율 계산: 위와 동일한 방식으로 계산됨
                                   // manRate와 womanRate의 합은 이론적으로 100에 가까워야 하지만 반올림 때문에 정확히 100이 아닐 수 있음

    /* 결과 출력 */
    printf("남자의 수는 %d이고 여자의 수는 %d명이다.\n", man, woman);
    // 위 printf 설명:
    // - 포맷 문자열에서 %d 자리에는 뒤의 man, woman 값이 순서대로 들어감
    // - 이 줄은 화면에 입력된 두 값의 확인(에코) 역할을 함. 다른 변수에는 영향을 주지 않음

    printf("총 수는 %.f명이고 남자의 비율은 %.2f%%이고 여자의 비율은 %.2f%%이다.\n", sum, 
    manRate, womanRate);
    // 위 printf 설명(자세히):
    // - "%.f": sum을 소수점 없이(반올림) 출력함. %.f는 double 값을 소수점이 없는 형태로 보여줌
    // - "%.2f": 소수점 둘째 자리까지 표시. 예: 43.27
    // - "%%": 출력할 때 실제 % 문자를 표시하기 위한 이스케이프(포맷 문자열 안에서 %는 %%로 표기)
    // - 이 printf는 화면 표시만 하고 프로그램의 계산 상태에는 영향을 주지 않음

    return 0; // main이 호출자(운영체제)에게 0을 반환하면 '정상 종료'를 의미함
}