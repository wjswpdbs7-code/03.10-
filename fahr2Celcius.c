/* 이전 코드 (수정 전)
// Fahr 온도값을 넣어서 Celcius 온도값으로 변환하도록 코드를 작성
#include<stdio.h>

int main()
{
    int fahr;
    printf("화씨 온도를 입력하세요");
    scanf("%d", fahr);
    double celsius;
    celsius = 9.0 / 5.0 * (double)fahr - 32.0;
    
    printf("C(celsius) 온도: %.2f\n", celsius); 
    return 0; 
}
*/

/*
 차이점 요약 (수정 전 vs 수정 후)

 - scanf 인자:
     수정 전: `scanf("%d", fahr)` -> 변수의 주소를 전달하지 않아 세그멘테이션 폴트 발생
     수정 후: `scanf("%d", &fahr)` -> 올바르게 주소를 전달하여 입력을 저장함

 - 변환 공식:
     수정 전: `9.0 / 5.0 * (double)fahr - 32.0` -> 연산 순서가 잘못되어 결과가 틀림
     수정 후: `(fahr - 32) * 5.0 / 9.0` -> 표준 화씨→섭씨 공식으로 정확히 계산

 - 타입과 연산:
     수정 전: 일부 연산에서 형 변환이 불분명하여 정수 연산으로 계산될 위험이 있음
     수정 후: 실수 리터럴(5.0, 9.0)을 사용해 소수점 계산을 보장

 - 주석과 가독성:
     수정 전: 주석이 부족해 초심자가 원인을 파악하기 어려움
     수정 후: 파일 상단과 각 줄에 설명을 추가해 학습용으로 보기 쉽게 개선

 - 함수 서명/스타일:
     수정 전: `int main()` 사용
     수정 후: `int main(void)`로 명시적 표기 및 코드 스타일 정리

*/

/*
 파일: fahr2Celcius.c
 역할: 사용자로부터 화씨(℉) 온도를 입력받아 섭씨(℃)로 변환하여 출력하는 예제
 배경지식: scanf로 값을 읽을 때는 변수의 주소(&변수)를 전달해야 합니다.
           화씨를 섭씨로 변환하는 공식은: C = (F - 32) * 5 / 9
 초심자 설명: 먼저 화씨를 입력받고, 올바른 변환식을 적용해 섭씨로 계산한 뒤 출력합니다.
*/

#include <stdio.h>

int main(void)
{
    int fahr; // 화씨 온도를 저장할 정수형 변수

    printf("화씨 온도를 입력하세요: "); // 사용자에게 입력을 요청
    scanf("%d", &fahr); // scanf에는 변수의 주소를 전달해야 함(&fahr)

    double celsius; // 변환된 섭씨 온도를 저장할 실수형 변수
    // 화씨 -> 섭씨 공식: (F - 32) * 5.0 / 9.0
    celsius = (fahr - 32) * 5.0 / 9.0; // 연산에서 실수형을 사용해 소수점까지 계산

    printf("C(celsius) 온도: %.2f\n", celsius); // 결과를 소수점 둘째 자리까지 출력
    return 0; // 정상 종료
}