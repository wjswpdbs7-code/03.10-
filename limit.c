#include <stdio.h> // 이 문구의 의미는 쉽게 풀어말하자면, 표준 입출력 라이브러리를 포함하는 것입니다. 
// 이 라이브러리는 printf와 같은 함수를 사용할 수 있게 해줍니다. 
#include <limits.h>
// 이 문구의 의미는 쉽게 풀어말하자면, 정수형 타입의 최소값과 최대값을 정의하는 매크로가 포함된 라이브러리를 포함하는 것입니다.
// 이 라이브러리를 포함하면 INT_MIN, INT_MAX, LONG_MIN, LONG_MAX, LLONG_MIN, LLONG_MAX 등의 매크로를 사용할 수 있습니다.
// 한번에 여러 라이브러리를 포함하는 것은 프로그램에서 다양한 기능을 사용할 수 있게 해주는 좋은 방법입니다.
// 이 문구가 없으면 int를 못쓰는가? 라고 생각할 수 있지만, 실제로는 int를 사용할 수는 있지만, int의 최소값과 최대값을 알 수 없게 됩니다.

/*
 파일: limit.c
 역할: 롤(타입)별 최소/최대값과 char 비트수를 출력하는 예제
 배경지식: <limits.h>에는 정수형 타입의 최소/최대 값을 나타내는 매크로가 정의되어 있습니다.
 초심자 설명: 각 정수형 타입의 범위를 확인하여 자료형의 한계를 이해할 수 있습니다.
*/
int main(void) //이 문구의 역할은 프로그램의 시작점을 정의하는 것입니다.
//이 문구가 없다면 컴파일러는 프로그램이 어디서 시작해야 하는지 알 수 없어서 오류가 발생합니다. int의 의미는 정수형을 나타내며,
// main은 프로그램의 진입점으로, 운영체제가 프로그램을 실행할 때 가장 먼저 호출하는 함수입니다. void는 main 함수가 인자를 받지 않는다는 것을 의미합니다. 
{//인자란 프로그램이 실행될 때 외부에서 전달되는 값들을 말합니다.
    printf("int_min : %d\t int_max : %d\n", INT_MIN, INT_MAX); // int 범위 출력 세부적으로 각각의 단어를 설명하자면 
// printf는 표준 출력 함수로, 화면에 텍스트를 출력하는 데 사용됩니다.() 안에는 출력할 문자열과 함께, %d는 정수형 데이터를 출력할 때 사용되는 형식 지정자입니다.
// INT_MIN과 INT_MAX는 <limits.h>에 정의된 매크로로, int 타입의 최소값과 최대값을 나타냅니다. 좀더 쉽게 설명하자면, 이 줄은 int 타입이 가질 수 있는
// 가장 작은 값과 가장 큰 값을 화면에 출력하는 역할을 합니다. 어떨때 사용하냐면, 예를 들어 int 타입의 변수에 어떤 값을 저장할 때, 그 값이 int의 범위를 
// 벗어나지 않는지 확인할 때 유용합니다. 좀더 비유해서 쉽게 설명하자면, int 타입은 마치 작은 상자와 같아서, 그 상자에 담을 수 있는 물건의 크기가 정해져 있습니다. 
// 이 줄은 그 상자가 담을 수 있는 가장 작은 물건과 가장 큰 물건의 크기를 알려주는 역할을 합니다. 코딩할때 자주 사용하는지 여부 는 상황에 따라 다르지만,
// 일반적으로는 int 타입의 범위를 확인할 필요가 있을 때 자주 사용됩니다. 알고있다면 사용할 필요가 없지만, 모른다면 자주 사용하게 될 것입니다.
    printf("long_min : %ld \t long_max : %ld\n", LONG_MIN, LONG_MAX); // long 범위 출력
    printf("long_long_min : %lld \t long_long_max : %lld\n", LLONG_MIN, LLONG_MAX); // long long 범위 출력
    printf("END_HELLO\n"); // 추가 출력(샘플 종료 표시)  무슨뜻이냐면 END_HELLO는 단순히 출력되는 문자열로, 프로그램의 출력이 끝났음을 나타내는 역할을 합니다.
    return 0; // 정상 종료
}
//예상 출력값은  
// int_min : -2147483648	 int_max : 2147483647
// long_min : -9223372036854775808	 long_max : 9223372036854775807
// long_long_min : -9223372036854775808	 long_long_max : 9223372036854775807
// END_HELLO
//오류는 없을 것으로 예상됩니다.

//int,long,long long의 범위로 나뉘는 이유는 각각의 타입이 저장할 수 있는 데이터의 크기가 다르기 때문입니다.
//int는 일반적으로 4바이트(32비트)를 사용하여 -2,147,483,648에서 2,147,483,647까지의 값을 저장할 수 있습니다.
//long은 일반적으로 8바이트(64비트)를 사용하여 -9,223,372,036,854,775,808에서 9,223,372,036,854,775,807까지의 값을 저장할 수 있습니다.
//long long은 long보다 더 큰 범위를 제공하며, 일반적으로 8바이트(64비트)를 사용하여 -9,223,372,036,854,775,808에서 9,223,372,036,854,775,807까지의 값을 저장할 수 있습니다.
//이러한 타입의 범위는 시스템과 컴파일러에 따라 다를 수 있지만, 일반적으로 위와 같은 범위를 갖습니다. 각각의 타입은 특정한 용도에 맞게 설계되었으며, 필요한 범위에 따라 적절한 타입을 선택하여 사용할 수 있습니다. 예를
// 주로 사용하는 코딩을 처음 배울때 int 타입이 가장 많이 사용됩니다. long과 long long은 더 큰 범위가 필요할 때 사용됩니다. 예를 들어, 큰 수를 다루는 프로그램이나, 
// 메모리 주소를 저장하는 경우에는 long이나 long long을 사용할 수 있습니다. 하지만 일반적으로는 int 타입이 충분한 경우가 많아서, 처음 배우는 사람들은 int를 주로 사용하게 됩니다.