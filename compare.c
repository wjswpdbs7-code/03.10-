#include <stdbool.h>
#include <stdio.h>

/*
 이 파일의 기능 요약 (한 문장)
  - 두 개의 정수를 입력받아 첫 번째가 두 번째보다 큰지 비교하고 결과를 화면에 출력합니다.

 배경지식 (초심자용, 쉽게 풀이)
 1) 변수와 자료형
    - `int`는 정수를 저장하는 자료형입니다. 예: 1, -3, 42 등.
    - `bool`은 참/거짓을 저장하는 자료형으로 <stdbool.h>에 정의되어 있습니다.
      값은 `true` 또는 `false`입니다. C에서 내부적으로는 1(true)과 0(false)로 표현됩니다.

 2) 입력과 출력
    - `printf`는 화면에 글자를 출력합니다.
    - `scanf`는 키보드로 입력한 값을 읽어 변수에 저장합니다. 포맷 문자열에 맞춰 값을 읽습니다.
      예: "%d %d"는 정수 두 개를 공백으로 구분해 읽겠다는 뜻입니다.

 3) 비교 연산자
    - `a > b`는 a가 b보다 큰지를 묻는 비교식입니다. 참이면 true(1), 거짓이면 false(0)가 됩니다.

 4) 조건문
    - `if (조건) { ... } else { ... }`는 조건이 참이면 if 블록을 실행하고,
      거짓이면 else 블록을 실행합니다.

 이 코드를 따라하면: 사용자로부터 두 정수를 받아 첫 번째가 더 큰지 아닌지 친절히 알려줍니다.
*/

int main(void)
{
    /* 두 정수를 저장할 변수를 선언합니다. */
    int a, b; // a와 b는 사용자가 입력할 정수를 담을 이름표(변수)입니다.

    /* 비교 결과를 저장할 불리언 변수를 선언하고 초기값을 false로 합니다. */
    bool greater = false; // greater는 "a가 b보다 큰가"라는 질문의 답을 담습니다.

    /* 사용자에게 입력을 요청하는 안내 문구를 출력합니다. */
    printf("두 정수를 입력하세요: ");

    /* scanf는 키보드 입력을 읽어 변수에 저장합니다.
       "%d %d"는 정수 두 개를 순서대로 읽겠다는 의미이고,
       &a, &b는 각각 a와 b 변수의 메모리 주소를 전달합니다. */
    scanf("%d %d", &a, &b);

    /* a가 b보다 큰지 비교 연산을 수행합니다.
       결과는 true 또는 false가 되어 greater에 저장됩니다. */
    greater = a > b;

    /* greater는 내부적으로 1(true) 또는 0(false)이므로 %d로 출력하면 숫자로 보입니다.
       이 줄은 비교 결과의 내부 값(0 또는 1)을 먼저 보여줍니다. */
    printf("greater의 값은 %d이다\n", greater);

    /* if 문을 사용해 greater가 true인지 확인합니다.
       C에서는 true는 0이 아닌 값, false는 0으로 취급됩니다.
       여기서는 명시적으로 '== true'를 써서 읽기 쉽게 표현했습니다. */
    if (greater == true)
    {
        /* 조건이 참이면(즉 a가 b보다 크면) 이 문장이 실행됩니다. */
        printf("a가 더 크다.\n");
    }
    else
    {
        /* 조건이 거짓이면(즉 a가 b보다 크지 않으면) 이 문장이 실행됩니다.
           이 경우 a가 b보다 작거나 같다는 의미입니다. */
        printf("a가 더 작다.\n");
    }

    /* 프로그램을 정상적으로 종료합니다. */
    return 0;
}