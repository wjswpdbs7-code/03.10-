#include <stdio.h> //이 문구가 있어야 이 파일에 미치는 영향을 설명할 수 있습니다. printf 함수를 사용하기 위해 stdio.h 헤더 파일을 포함합니다.
// stdio.h는 이 파일에서 printf의 역할만 제공하는 것이 아니라, C 표준 입출력 라이브러리를 포함하여 다양한 입출력 기능을 사용할 수 있게 해줍니다.
//이 파일 내부에서 printf제외하고 다른 stdio.h의 기능은 사용되지 않지만, printf를 통해 다양한 리터럴을 출력하는 예제를 보여줍니다.
/*
 파일: literal.c
 역할: 다양한 리터럴(10진, 8진, 16진, 정수형 접미사, 실수형, 문자, 문자열) 출력 예제
 초등학생도 이해하기 쉽게 설명하자면 이 프로그램은 숫자와 문자, 그리고 문자열을 다양한 방식으로 표현하는 방법을 보여주는 예제입니다.

 배경지식: 리터럴은 코드 안에 직접 쓰인 값입니다. 예: 100, 0144(8진), 0x64(16진), 'A', "ABCD"
 초심자 설명: 같은 값을 서로 다른 표기법으로 표현했을 때 C가 이를 어떻게 처리하는지 보여줍니다.
*/

int main(void)//만약 void 대신 다른 인자가 사용되지 않는 이유를 간단하고 쉽게 설명하자면, main 함수는 프로그램의 시작점입니다.
//  void는 main 함수가 인자를 받지 않는다는 것을 의미합니다. 즉, 이 프로그램은 명령줄 인자를 사용하지 않고 실행된다는 것을 나타냅니다.
//  만약 int argc, char *argv[]와 같은 인자를 사용했다면, 프로그램이 명령줄에서 인자를 받을 수 있게 됩니다. 
// 하지만 이 예제에서는 그런 기능이 필요 없기 때문에 void를 사용하여 간단하게 표현한 것입니다.
{
    printf("%d %d %d\n", 100, 0144, 0x64); // ()안의 글자인 %d %d %d\n", 100, 0144, 0x64 라는 글자가 나오는게 아니라 100 100 100으로 나오는 이유는, 
    // printf 함수는 첫 번째 인자로 전달된 형식 문자열을 해석하여 나머지 인자들을 해당 형식에 맞게 출력합니다.
    // 무슨소리냐면 %d는 10진수로 출력하라는 지시이므로, printf는 100, 0144, 0x64를 모두 10진수로 해석하여 출력합니다.
    //\n은 줄바꿈을 의미하여, 출력 후 다음 줄로 이동하게 됩니다.
    // 그냥 100 0144 0x64라는 글자가 출력되게하고싶다면 printf("100 0144 0x64\n"); 이렇게 하면 됩니다.
    //""는 문자열 리터럴을 나타내며, printf 함수는 이 문자열을 그대로 출력합니다.
    //만약 이 줄에서""만 빠진다면 printf(%d %d %d\n, 100, 0144, 0x64); 이렇게 되는데, 이 경우에는 컴파일 오류가 발생합니다. 
    // 왜냐하면 printf 함수는 첫 번째 인자로 형식 문자열이 필요하기 때문입니다. 
    //("%d %d %d\n", 100, 0144, 0x64) 구조는 printf 함수가 형식 문자열과 그에 따른 인자들을 올바르게 해석하여 출력할 수 있도록 하는 표준적인 방식입니다.
    //,는 인자들을 구분하는 역할을 합니다. 첫 번째 인자는 형식 문자열이고, 그 뒤에 오는 인자들은 형식 문자열에서 지정된 형식에 맞게 출력됩니다.
    // 순서대로 대입하는 원리 는, printf 함수는 형식 문자열에서 %d와 같은 형식 지정자를 찾을 때마다, 그에 해당하는 인자를 순서대로 가져와서 출력합니다.
    // 예를 들어, 첫 번째 %d는 100에 해당하고, 두 번째 %d는 0144에 해당하며, 세 번째 %d는 0x64에 해당합니다. 따라서 출력 결과는 100 100 100이 됩니다.
    //그렇게 정해진건 어디에서 알 수 있냐면, C 언어의 표준 라이브러리에서 printf 함수의 동작 방식이 정의되어 있습니다.
    //%d는 10진수로 출력하라는 형식 지정자입니다.0144가 100인 이유는 0144는 8진수 리터럴이기 때문입니다. 
    // 8진수란 0부터 7까지의 숫자를 사용하여 표현하는 숫자 체계입니다. 0144는 8진수로 표현된 숫자이며, 이를 10진수로 변환하면 100이 됩니다.
    // 직접 계산해야 하는가? 라고 생각할 수 있지만, 실제로는 C 컴파일러가 0144를 8진수로 해석하여 자동으로 10진수로 변환해줍니다.
    // 100은 10진수 리터럴입니다. 이를 쉽게 설명하자면, 우리가 일상적으로 사용하는 숫자 체계인 10진수로 표현된 숫자입니다. 0144는 8진수 리터럴입니다.
    //  8진수는 0부터 7까지의 숫자를 사용하여 표현하는 숫자 체계입니다. 0x64는 16진수 리터럴입니다. 
    // 16진수는 0부터 9까지의 숫자와 A부터 F까지의 문자를 사용하여 표현하는 숫자 체계입니다.
    //첫번째 두번째 둘다 %d인데 하나는 10진수로 쓰고 다른하나는 8진수인 이유는, C언어에서 숫자 앞에 0이 붙으면 8진수로 해석되고, 0x가 붙으면 16진수로 해석되기 때문입니다.
    //실제 코딩할때 내가 직접 10진수로 생각한 숫자를 8진수나 16진수로 직접 계산해야하는경우가 많은가요? 라고 생각할 수 있지만, 실제로는 그런 경우는 드뭅니다.
    // 대부분의 경우에는 10진수로 숫자를 작성하고, 필요에 따라 8진수나 16진수로 표현하는 경우가 많습니다.
    printf("%d 0%o 0x%x\n", 100, 100, 100); // 100을 10진, 8진(앞에 0), 16진(앞에 0x)으로 출력
    // %d는 10진, %o는 8진, %x는 16진 ""을  붙이는 경우는 문자열 리터럴로 인식되어 그대로 출력됩니다. 문자열 리터럴이란 비유하자면 
    // 

    printf("%lldLL\n", 1000000000); // LL 접미사가 붙은 long long 리터럴 출력
    // ll는 long long 타입을 의미
    //이거 앞에 "%lldLL\n" 있을 필요가 있나 값이 같은데 라고 생각할 수 있지만, 실제로는 "%lldLL\n"은 long long 타입의 정수 리터럴을 출력하기 위한 형식 지정자입니다.
    // 1000000000LL은 long long 타입의 정수 리터럴입니다. 이 형식 지정자와 리터럴이 함께 사용되어야 printf가 long long 타입의 값을 올바르게 해석하여 출력할 수 있습니다. 만약 "%lldLL\n"
    //%lld는 long long 타입의 정수를 10진수로 출력하라는 형식 지정자입니다. 1000000000LL은 long long 타입의 정수 리터럴입니다. 
    // 뭔소리냐면, 1000000000은 우리가 일상적으로 사용하는 숫자 체계인 10진수로 표현된 숫자입니다. LL 접미사는 이 숫자가 long long 타입의 정수 리터럴임을 나타냅니다.
   // 출력값은 1000000000LL이 됩니다. 그럼 값의 변화가 없다는 뜻인가요? 라고 생각할 수 있지만, 실제로는 1000000000LL은 long long 타입의 정수 리터럴이므로, 
   // 출력값은 1000000000이 됩니다.ll 이 무슨뜻이냐면 long long 타입의 정수 리터럴을 나타내는 접미사입니다. C 언어에서 정수 리터럴에 LL을 붙이면, 해당 숫자가 long long 타입으로 해석됩니다.
   //long long타입이란 long보다 더 큰 범위를 제공하는 정수 타입입니다. 일반적으로 8바이트(64비트)를 사용하여 -9,223,372,036,854,775,808에서 9,223,372,036,854,775,807까지의 값을 저장할 수 있습니다.
    printf("%f %f\n", 2.718, 3.141592F); // 실수 리터럴(기본 double, F는 float 접미사) 
    // %f는 실수형 데이터를 10진수로 출력하라는 형식 지정자입니다.
    printf("%d %d %d\n", 'A', 'a', '0'); // 문자의 정수(아스키 코드) 값을 출력
    printf("%c %c %c\n", 65, 97, 48); // 정수값을 문자로 출력(아스키 코드에 해당하는 문자)
    printf("%c %c %c\n", 'A', 'A' + 1, 'A' + 2); // 문자 연산: 'A'+1 => 'B'
    printf("%s\n", "ABCD"); // 문자열 리터럴 출력
    return 0; // 정상 종료
}
//예상 출력값은 
// 100 100 100
// 100 0144 0x64
// 1000000000LL
// 2.718000 3.141592
// 65 97 48
// A a 0
// A B C
// ABCD
