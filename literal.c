#include <stdio.h>

/*
 파일: literal.c
 역할: 다양한 리터럴(10진, 8진, 16진, 정수형 접미사, 실수형, 문자, 문자열) 출력 예제
 배경지식: 리터럴은 코드 안에 직접 쓰인 값입니다. 예: 100, 0144(8진), 0x64(16진), 'A', "ABCD"
 초심자 설명: 같은 값을 서로 다른 표기법으로 표현했을 때 C가 이를 어떻게 처리하는지 보여줍니다.
*/

int main(void)
{
    printf("%d %d %d\n", 100, 0144, 0x64); // 10진 100, 8진 0144, 16진 0x64 모두 값 100 출력
    printf("%d 0%o 0x%x\n", 100, 100, 100); // 100을 10진, 8진(앞에 0), 16진(앞에 0x)으로 출력
    // %d는 10진, %o는 8진, %x는 16진

    printf("%lldLL\n", 1000000000LL); // LL 접미사가 붙은 long long 리터럴 출력
    // ll는 long long 타입을 의미

    printf("%f %f\n", 2.718, 3.141592F); // 실수 리터럴(기본 double, F는 float 접미사)
    printf("%d %d %d\n", 'A', 'a', '0'); // 문자의 정수(아스키 코드) 값을 출력
    printf("%c %c %c\n", 65, 97, 48); // 정수값을 문자로 출력(아스키 코드에 해당하는 문자)
    printf("%c %c %c\n", 'A', 'A' + 1, 'A' + 2); // 문자 연산: 'A'+1 => 'B'
    printf("%s\n", "ABCD"); // 문자열 리터럴 출력
    return 0; // 정상 종료
}
