#include <stdio.h>

/*
  배경지식 (초심자용, 쉽게 풀이)

  1) 프로그램의 시작 지점: main
    - 모든 C 프로그램은 `main` 함수에서 시작합니다. 여기서부터 한 줄씩 실행됩니다.

  2) 변수란 무엇인가?
    - 변수는 '이름표 붙은 그릇'입니다. 값을 담아두고 필요할 때 꺼내 씁니다.
    - `int`는 정수를 담는 그릇을 만든다는 의미입니다.

  3) 입력과 출력
    - `printf`는 화면에 글자를 보여줄 때 사용합니다.
    - `scanf`는 사용자가 키보드로 입력한 값을 읽어 변수에 저장합니다.
     이때 `&`는 그릇(변수)의 '위치'를 알려 주는 표시입니다.

  4) 나머지 연산자 `%`
    - `a % b`는 a를 b로 나눈 뒤 남는 수(나머지)를 구합니다.
    - 예: 7 % 2 = 1(홀수), 8 % 2 = 0(짝수).

  5) 조건문 if
    - `if (조건) { ... } else { ... }`는 조건이 참인지 거짓인지에 따라
     서로 다른 문장을 실행합니다. C에서는 0을 '거짓', 0이 아닌 값을 '참'으로 봅니다.

  이 프로그램의 목적: 사용자가 입력한 정수가 홀수인지 짝수인지 판별해 친절히 알려줍니다.
*/

int main(void)
{
   /* 사용자가 입력할 숫자를 담을 그릇을 만듭니다. */
   int num; // num이라는 이름의 정수형 변수(그릇).

   /* 사용자에게 무엇을 입력해야 하는지 안내합니다. */
   printf("숫자를 입력하세요: ");

   /* 키보드로 입력한 정수를 읽어 num에 저장합니다.
     scanf의 "%d"는 정수 포맷을 의미하고, &num은 num이라는 그릇의 주소입니다. */
   scanf("%d", &num);

   /* 홀수인지 여부를 저장할 변수 선언 */
   int isOdd;

   /* num을 2로 나눈 나머지를 구해 isOdd에 저장합니다.
     나머지가 1이면 홀수, 0이면 짝수입니다. */
   isOdd = num % 2;

   /*
    아래의 if 문은 isOdd가 0이 아닌 값이면(참이면) 홀수라고 알려주고,
    그렇지 않으면(거짓이면) 짝수라고 알려줍니다.

    C에서 조건식에 정수를 쓰면 0은 '거짓(false)', 0이 아닌 값은 '참(true)'으로 취급합니다.
   */
   if (isOdd)
   {
      /* isOdd가 1(또는 0이 아닌 값)이면 이 부분이 실행됩니다. */
      printf("입력한 숫자는 홀수입니다.\n");
   }
   else
   {
      /* isOdd가 0이면(짝수면) 이 부분이 실행됩니다. */
      printf("입력한 숫자는 짝수입니다.\n");
   }

   /* 추가로 입력한 숫자와 isOdd 값을 함께 보여줍니다. \t는 탭(들여쓰기)입니다. */
   printf("숫자는: %d \t is isOdd: %d\n", num, isOdd);

   /* 프로그램을 정상적으로 끝냈음을 운영체제에 알리는 값(0)을 반환합니다. */
   return 0; 
}
