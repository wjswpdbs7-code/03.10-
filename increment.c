#include <stdio.h>

//이 파일은 C 언어에서 증감 연산자(++와 --)의 사용법을 보여주는 예제입니다.
// 증감 연산자는 변수의 값을 1씩 증가시키거나 감소시키는 데
// 사용됩니다. 이 예제에서는 ++a와 a++의 차이점을 보여줍니다.
// ++a는 전위 증가 연산자로, a의 값을 먼저 증가시키고 그 결과를 사용합니다.
// a++는 후위 증가 연산자로, a의 현재 값을 먼저 사용하고 그 후에 a의 값을 증가시킵니다.

int main(void)
{
  int a, b; //; 붙인이유는 C에서 각 명령문(statement)의 끝을 나타내기 위해서입니다. 정수형 변수란 정수 값을 저장할 수 있는 변수입니다. 정수란 소수점이 없는 숫자를 말합니다. 예를 들어, 1, -5, 100 등이 정수입니다. a와 b는 정수형 변수로 선언되어 있습니다. 
  //이말은 a,b는 무조건 정수값만 저장할 수 있다는 뜻입니다. 만약 a나 b에 소수점이 있는 숫자를 저장하려고 하면, 컴파일러가 오류를 발생시킬 것입니다.
  b = ++a; // a의 값을 1 증가시키고 그 결과를 b에 저장 이로인해 a는 6이 되고 b도 6이 됩니다. a가 6인 이유는 
  printf("a : %d, \t b : %d\n", a, b); // a는 6, b도 6이 출력됨 :는 a와 b의 값을 구분하는 역할을 합니다.
  // \t는 탭 문자를 나타내며, 출력에서 a와 b 사이에 일정한 간격을 만들어줍니다. 
  // %d는 정수형 데이터를 10진수로 출력하라는 형식 지정자입니다.
  
  a = 5; //여기서 a를 다시 5로 초기화 이유는 이전에 a가 6이 되었지만, 이번에는 a를 5로 초기화하여 후위 증가 연산자(a++)의 효과를 보여주기 위해서입니다.
  b = a++; // a의 현재 값을 b에 저장한 후 a의 값을 1 증가시킴
  printf("a : %d, \t b : %d\n", a, b); // a는 6이지만 b는 5가 출력

  printf("a : %d, \t b : %d\n", a, a++); // a는 6, b는 5가 출력 
  printf("a : %d, \t b : %d\n", a, ++a); // a는 7, b도 7이 출력

  a = 10;// 지금 b값은 5입니다.b도 초기화 하려면 
  printf("a : %d, \t b : %d\n", a, ++b);//a : %d같은 문구를 부르는 명칭이 뭐

  return 0; // 정상 종료 
  
}
//출력값은
// a : 6, 	 b : 6
// a : 6, 	 b : 5
// a : 6, 	 b : 6
// a : 7, 	 b : 7
