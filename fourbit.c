//파일의 기능은 4비트 마스크 연산을 보여주는 간단한 예제입니다.
//C17 표준을 사용하여 작성되었습니다.
//초심자에게는 비트 연산이 낯설 수 있지만, 이 예제는 XOR, OR, AND 연산을 통해 어떻게 특정 비트를 설정하거나 해제할 수 있는지 보여줍니다.
// 국어적 예시를 들어 서명하자면 "attr"이라는 이름의 8비트(1바이트) 그릇이 있다고 생각해보세요. 이 그릇은 0과 1로 이루어진 8개의 칸이 있습니다.
// 처음에는 모든 칸이 0으로 비어있습니다.
// 첫 번째 연산은 XOR을 사용하여 attr의 모든 비트를 0으로 초기화합니다. (attr = attr ^ attr;)
// 두 번째 연산은 OR을 사용하여 attr의 하위 4비트를 1로 설정합니다. (attr = attr | 0b00001111;)
// 세 번째 연산은 OR을 사용하여 attr의 하위 2비트를 1로 설정합니다. (attr = attr | 0b00000011;)
// 네 번째 연산은 AND와 NOT을 사용하여 attr의 최하위 비트를 0으로 설정합니다. (attr = attr & (~0b00000001);)
// 각 단계에서 attr의 상태를 16진수로 출력하여 어떻게 비트가 변경되는지 확인할 수 있습니다.

#include<stdio.h>
#include <stdint.h>

int main(void)
{
    uint8_t attr;
    attr = attr ^ attr;
    attr = attr | 0b00001111;
    printf("attr: 0x%02x\n", attr);
    attr = attr | 0b00000011;
    printf("attr: 0x%02x\n", attr);
    attr = attr & (~0b00000001);
    printf("attr: 0x%02x\n", attr);
    return 0;
}

// 이 코드 예상 출력값은 다음과 같습니다:
// attr: 0x0f
// attr: 0x0f
// attr: 0x0e
//각각의 의미는 첫 번째 출력은 0b00001111 (0x0f)으로 하위 4비트가 모두 1로 설정된 상태입니다.
//두 번째 출력은 이미 하위 4비트가 모두 1이므로 변화가 없습니다.
//세 번째 출력은 최하위 비트가 0으로 설정되어 0b00001110 (0x0e)으로 변경된 상태입니다.